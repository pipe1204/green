export function EmojiPicker({
  onEmojiSelect,
  onClose,
  isOpen,
}: EmojiPickerProps) {
  if (!isOpen) return null;

  return (
    <div
      className="absolute bottom-full left-0 mb-2 bg-white border border-gray-200 rounded-lg shadow-xl p-3 z-50 w-80 max-h-80"
      key="emoji-picker"
    >
      <div className="space-y-3">
        {/* Header */}
        <div className="flex items-center justify-between">
          <h4 className="text-sm font-medium text-gray-900">Emojis</h4>
          <Button
            variant="ghost"
            size="sm"
            onClick={onClose}
            className="h-6 w-6 p-0"
          >
            ×
          </Button>
        </div>

        {/* Emoji Categories */}
        <div className="space-y-2 max-h-64 overflow-y-auto">
          {emojiCategories.map((category) => (
            <div key={category.name}>
              <h5 className="text-xs font-medium text-gray-500 mb-1 px-1">
                {category.name}
              </h5>
              <div className="grid grid-cols-10 gap-0.5">
                {category.emojis.map((emoji, index) => (
                  <button
                    key={index}
                    onClick={() => onEmojiSelect(emoji)}
                    className="w-7 h-7 flex items-center justify-center rounded hover:bg-gray-100 text-base transition-colors"
                    title={emoji}
                  >
                    {emoji}
                  </button>
                ))}
              </div>
            </div>
          ))}
        </div>
      </div>
    </div>
  );
}
